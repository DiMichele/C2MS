/**
 * Cerca il codice catastale di un comune
 * @param {string} comune - Nome del comune
 * @param {string} provincia - Sigla della provincia (opzionale)
 * @returns {string|null} - Codice catastale o null se non trovato
 */
function cercaCodiceComune(comune, provincia = '') {
    if (!comune) return null;
    
    // Normalizza il nome del comune
    const comuneNorm = comune.toUpperCase().trim()
        .replace(/'/g, "'")
        .replace(/`/g, "'")
        .replace(/'/g, "'");
    
    // Cerca direttamente
    if (ComuniItaliani[comuneNorm]) {
        return ComuniItaliani[comuneNorm];
    }
    
    // Cerca con varianti comuni
    const varianti = [
        comuneNorm,
        comuneNorm.replace(/ /g, '-'),
        comuneNorm.replace(/-/g, ' '),
        comuneNorm.replace(/SANT'/g, "SANT'"),
        comuneNorm.replace(/SAN /g, "S. "),
        comuneNorm.replace(/S\. /g, "SAN "),
        comuneNorm.replace(/SANTA /g, "S. "),
        comuneNorm.replace(/SANTO /g, "S. ")
    ];
    
    for (const variante of varianti) {
        if (ComuniItaliani[variante]) {
            return ComuniItaliani[variante];
        }
    }
    
    // Cerca parziale (inizio del nome)
    for (const [nomeComune, codice] of Object.entries(ComuniItaliani)) {
        if (nomeComune.startsWith(comuneNorm) || comuneNorm.startsWith(nomeComune)) {
            return codice;
        }
    }
    
    return null;
}

